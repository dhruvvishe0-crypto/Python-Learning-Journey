# variables are the containers that hold data values 
# example of variables : 
age = 24 # this is an integer variable
print(age)

pointer = 9.5 # this is a float variable
print(pointer )

name = ("Dhruv") # this is a string value  
print(name)

# Rules for naming variables:
# 1. variables can contain only letter, numbers,underscore
# 2. Variables cannot start with a number 
# 3. Variables are case sensitive 
# 4. Variables cannot start with a special character and also cannot involve special characters except the underscore
# 5. Variables can start with a capital letter or small letter 
# 6. Variables cannot be contain a reserved keyword in python like print, input, if, else, for, while, etc.
# 7. String Values can be enclosed in a single quote or double quote and there is no need to use brackets for string values
# 8. Variables can be assigned multiple values in a single line using commas
# 9. Variables can be reassigned to a new value at any time
# 10. Variables can be used in mathematical operations

Laptop_Weight = 2.5 
print("the weight of laptop is", Laptop_Weight)

Laptop_Model = ("HP Victus 15")#This is a String variable
print ("the Model of laptop is ", Laptop_Model)
print("The Model and Weight of a laptop is respectively", Laptop_Model,Laptop_Weight)