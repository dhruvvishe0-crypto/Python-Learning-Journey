#  Escape Sequences


# Escape sequences are used to include special characters in strings.
#  Common escape sequences:

# \n : Newline
#  \t : Tab
#  \\ : Backslash
#  \" : Double quote
#  \' : Single quote
#  Example:

print("How Are You? \n I am Fine .") # New line
print("How are You ? \t I am Fine.") # Tab space
print("How are You ? \\ I am Fine.") # Backslash
print("How are You ? \" I am Fine.") # Double quote
print("How are You ? \'I am Fine.")  # Single quote

# separator in print function:
# The separator in the print function is used to separate multiple values in the output.
# The default separator is a space.
# Example:

print("Hello World ", "Dhruv") # Output: Hello World Dhruv
print("Hello World", "Dhruv", sep = "...") # Output: Hello World...Dhruv

# end in print function:
# The end in the print function is used to specify what to print at the end of the output.
# The default end is a newline character.
# Example:
print("Hello World", end = "....")
print("Dhruv", end = ("!"))



